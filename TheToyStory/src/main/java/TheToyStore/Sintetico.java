    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        System.out.println("teste");
        
        try{  
            Class.forName("com.mysql.jdbc.Driver");  
            Connection con=DriverManager.getConnection( "jdbc:mysql://localhost:3399/toystore?useTimezone=true&serverTimezone=America/Sao_Paulo","root","1989");  
            
            Date dateI = dataIni.getDate( );
            Date dateF = dataFim.getDate( );
            
            SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");  
            
            String dataInicial = formatter.format( dateI );
            String dataFinal   = formatter.format( dateF );
            
            Statement stmt=con.createStatement();  
            ResultSet consultaTbVenda=stmt.executeQuery("SELECT data_venda, numero_venda, id_cliente, SUM( valor_produto * quantidade_produto ) FROM venda WHERE data_venda BETWEEN '" + dataInicial + "' AND '" + dataFinal + "' GROUP BY numero_venda;");  
            
            DefaultTableModel model = (DefaultTableModel) relatorio.getModel( );            
            model.setRowCount(0);
            
            double valorTotal = 0;
            
            while(consultaTbVenda.next()){
                String data_compra   = consultaTbVenda.getString(1);
                String numero_venda  = consultaTbVenda.getString(2);
                String id_cliente    = consultaTbVenda.getString(3);
                String valor_venda   = consultaTbVenda.getString(4);
                
                Statement stmt2=con.createStatement();  
                ResultSet consultaTbCliente=stmt2.executeQuery("select nome from clientes where idCliente = '" + id_cliente + "'");  
                consultaTbCliente.next( );
                String nomeCliente = consultaTbCliente.getString(1);
                consultaTbCliente.close( );
                stmt2.close( );
                
                model.addRow( new Object[]{data_compra, numero_venda, nomeCliente, valor_venda} );
                
                valorTotal += Double.parseDouble( valor_venda );
            }
                
            String totalVendas = String.valueOf( valorTotal );
            valorTotalVendasR.setText( totalVendas );
                    
            consultaTbVenda.close( );
            stmt.close( );
            con.close( );  
            
            }
        catch(Exception e){ System.out.println(e);}      
    }//GEN-LAST:event_jButton1ActionPerformed

