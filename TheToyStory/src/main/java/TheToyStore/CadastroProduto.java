package TheToyStore;


public class CadastroProduto {
    
    private int codigoProduto;
    private String nomeProduto;
    private String fabricanteProduto;
    private int quantidadeProduto;
    private int valorProduto;
    private String pesquisarProduto;

    public int getcodigoProduto() {
        return codigoProduto;
    }

    public void setcodigoProduto(int codigoProduto) {
        this.codigoProduto = codigoProduto;
    }

    public String getnomeProduto() {
        return nomeProduto;
    }

    public void setnomeProduto(String nomeProduto) {
        this.nomeProduto = nomeProduto;
    }

    public String getfabricanteProduto() {
        return fabricanteProduto;
    }

    public void setfabricanteProduto(String fabricanteProduto) {
        this.fabricanteProduto = fabricanteProduto;
    }

    public int getquantidadeProduto() {
        return quantidadeProduto;
    }


    public void setquantidadeProduto( int quantidadeProduto) {
        this.quantidadeProduto = quantidadeProduto;
    }

    public int getvalorProduto() {
            return valorProduto;
    }

    public void setvalorProduto(int valorProduto) {
        this.valorProduto = valorProduto;
    }

    
    public String getpesquisarProduto() {
        return pesquisarProduto;
    }
    public void setpesquisarProduto(String pesquisarProduto) {
        this.pesquisarProduto = pesquisarProduto;
    }
    
    
}
